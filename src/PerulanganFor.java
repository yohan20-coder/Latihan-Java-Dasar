public class PerulanganFor {

    public static void main(String[] args) {
        //materi contoh perulangan for
        for (var angka = 1; angka <=10;){
            System.out.println("Perulangan Angka Ke "+angka);
            angka++;
        }
        //Meteri Contoh Perulangan dengan post statement
        for (var nilai = 1; nilai <=10; nilai++){
            System.out.println("Perulangan Nilai Ke "+nilai);
        }
    }

}
